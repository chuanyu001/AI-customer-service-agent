# 记录仪标准列结构 (顺序即此)
DASHCAM_COLUMNS = [
    "知识编号", "产品模块", "厂家", "知识类型", "标准问题", "常见问法",
    "标准答案", "润色答案", "是否需要识别品牌", "是否需要附件",
    "关联查询编号", "状态", "答复策略",
]

# 业务库源文件 → 输出文件名
BUSINESS = [
    ("折扣加油_Agent知识库.xlsx", "折扣加油知识库润色版6.24.xlsx"),
    ("WiFi套餐_Agent知识库.xlsx", "WiFi套餐知识库润色版6.24.xlsx"),
    ("基础流量处理_Agent知识库.xlsx", "基础流量处理知识库润色版6.24.xlsx"),
]


def remap_to_dashcam(df: pd.DataFrame) -> pd.DataFrame:
    """把业务库 01_知识问答库 重构为记录仪列结构"""
    out = pd.DataFrame(columns=DASHCAM_COLUMNS)
    out["知识编号"] = df["知识编号"].values
    out["产品模块"] = df["产品模块"].values
    out["厂家"] = ""                              # 业务库无厂家概念
    out["知识类型"] = df["功能分类"].values
    out["标准问题"] = df["标准问法"].values
    out["常见问法"] = df["相似问法"].values
    out["标准答案"] = df["标准答案"].values
    out["润色答案"] = df["知识编号"].map(lambda c: POLISH.get(str(c).strip(), "")).values
    out["是否需要识别品牌"] = df["是否需要识别类型"].values
    out["是否需要附件"] = "否"                     # 业务库无附件, 统一填否
    out["关联查询编号"] = df["关联查询规则"].fillna("").values
    out["状态"] = df["状态"].values
    out["答复策略"] = df["转人工规则"].values
    return out[DASHCAM_COLUMNS]


def convert_one(src_name: str, out_name: str):
    src = os.path.join(BASE, src_name)
    out = os.path.join(BASE, out_name)

    df01 = pd.read_excel(src, sheet_name="01_知识问答库")
    new01 = remap_to_dashcam(df01)

    # 00_使用说明 原样保留 (无表头, header=None)
    sheets = pd.read_excel(src, sheet_name=None)
    has_usage = "00_使用说明" in sheets

    with pd.ExcelWriter(out, engine="openpyxl") as writer:
        new01.to_excel(writer, sheet_name="01_知识问答库", index=False)
        if has_usage:
            usage = pd.read_excel(src, sheet_name="00_使用说明", header=None)
            usage.to_excel(writer, sheet_name="00_使用说明", index=False, header=False)

    # 校验: 润色答案是否全部填上
    empty_polish = new01[new01["润色答案"] == ""]
    return len(new01), list(empty_polish["知识编号"])


def main():
    for src_name, out_name in BUSINESS:
        n, missing = convert_one(src_name, out_name)
        msg = f"空润色: {missing}" if missing else "润色答案全部填充"
        print(f"✅ {out_name}: {n} 条, 13列结构, {msg}")

    # 记录仪润色版 → 另存为 6.24 命名 (列结构已对齐)
    dash_src = os.path.join(BASE, "行车记录仪Agent知识库_更新版622_润色版.xlsx")
    dash_out = os.path.join(BASE, "记录仪知识库润色版6.24.xlsx")
    shutil.copyfile(dash_src, dash_out)
    print(f"✅ 记录仪知识库润色版6.24.xlsx: 已从润色版另存 (13列结构, 含润色答案)")


if __name__ == "__main__":
    main()
